package main

import "fmt"

func main() {
	num1 := 25
	num2 := 13
	//addition
	sum := num1 + num2
	fmt.Println(sum)

	// substraction
	sub := num1 - num2
	fmt.Println(sub)

	// MULTIPLICATION
	mul := num1 * num2
	fmt.Println(mul)

	// DIVISION
	div := num1 / num2
	fmt.Println(div)

	//MODULUS
	Rem := num1 % num2
	fmt.Println(Rem)
}
